# Example If Else

a = 33
b = 200

if b > a:
    print("B e maior que A")
# a palavra "elif" é se as condições anteriores não forem atendidas tente essa condição
elif a == b:
    print("A e B são iguais!")
# Nessa condição o 'A' é igual a 'B', se a primeira condição não for verdadeira, torna o 'elif' 'true'

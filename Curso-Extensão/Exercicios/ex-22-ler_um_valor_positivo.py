# Exercício de Lógica de Programação utilizando Python
# Enunciado: Ler um valor e escrever se é positivo ou negativo (considere o valor zero como positivo)

# (Saída) - Apresentação programa
print("Programa que considerando o zero como positivo")
print()

# (Entrada) - Pedindo dados e armazenando
numero = int(input("Digite um número: "))

# (Processamento) - Verificando se é positivo e fazendo a Saída
if numero >= 0:
    print("Número é positivo")
else:
    print("Número negativo")

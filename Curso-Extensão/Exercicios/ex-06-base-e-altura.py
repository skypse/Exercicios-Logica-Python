# Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a área do retângulo.

# (Saída) - Apresentação
print("Programa que calcula área do retangulo")
print()

# (Entrada) - Pedindo os dados e armazenando
base = int(input('Digite a base do retângulo'))
altura = int(input('Digite a altura do retângulo'))

# (Processamento) - Fazendo o calculo da área do retângulo
calculo = base * altura

# (Saída) - Exibindo o resultado dos calculos
print("A área do retângulo é:",calculo)
# Enunciado: Escreva um algoritmo para ler dois valores do teclado (base e exponenciação) e exibir como resposta a exponenciação.

# Programa de calculo da área de um 'trapezio'

# (Saída) - Apresentação
print("Programa de Exponenciação")

# (Entrada) - Pedindo dados e armazenando
num1 = int(input("Digite a base: "))
num2 = int(input("Digite o expoente: "))

# (Processando) - Fazendo o calculo 
resultado = num1 ** num2

# (Saída) - Exibindo os resultados
print("O resultado da exponenciação foi: ", resultado)

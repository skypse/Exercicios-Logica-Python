# Programa de calculo da área de um 'triângulo'

# (Saída) - Apresenação
print("Programa que calcula área do triângulo")
print()

# (Entrada) - Pedindo os dados e armazenando
base = int(input('Digite a base do triângulo: '))
altura = int(input('Digite a altura do triângulo: '))

# (Processamento) - Fazendo o calculo da área do triângulo
calculo = base * altura

# (Saída) - Exibindo o resultado dos calculos
print("A área do triângulo é: ",calculo)
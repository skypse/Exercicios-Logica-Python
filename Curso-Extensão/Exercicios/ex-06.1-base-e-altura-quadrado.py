# (Saída) - Apresenação
print("Programa que calcula área do quadrado")
print()

# (Entrada) - Pedindo os dados e armazenando
base = int(input('Digite a base do quadrado: '))
altura = int(input('Digite a altura do quadrado: '))

# (Processamento) - Fazendo o calculo da área do quadrado
calculo = base * altura

# (Saída) - Exibindo o resultado dos calculos
print("A área do quadrado é: ",calculo)
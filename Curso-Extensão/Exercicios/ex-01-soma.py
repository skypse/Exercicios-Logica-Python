# Programa de Soma

# (Sáida) - Apresentação
print("Programa de Soma")
print()

# (Entrada) - Pedindo os dados e armazenando
num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

# (Processamento) - Efetuando a soma
resultado = num1 + num2

# (Saída) - Exibindo os resultados
print("O resultado da soma foi: ", resultado)
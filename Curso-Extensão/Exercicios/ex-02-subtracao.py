# Programa de Subtração

# (Sáida) - Apresentação
print("Programa de Subtração")
print()

# (Entrada) - Pedindo os dados e armazenando
num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

# (Processamento) - Efetuando a subtração
resultado = num1 - num2

# (Saída) - Exibindo os resultados 
print("O resultado da subtracao foi: ", resultado)